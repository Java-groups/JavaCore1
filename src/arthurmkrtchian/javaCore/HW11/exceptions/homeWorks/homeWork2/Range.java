package arthurmkrtchian.javaCore.HW11.exceptions.homeWorks.homeWork2;

public class Range {
    private int start;
    private int end;

    public Range(int start, int end) {
        this.start = start;
        this.end = end;
    }

    public Range() {
        this.start = 1;
        this.start = 100;
    }

    public int getStart() {
        return this.start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return this.end;
    }

}
